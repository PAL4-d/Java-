package learn.java编程基础;

public class switchlearn {
    public static void main(String[]args){
        int week = 5;
        switch(week)
        {
            case 1:
                System.out.println("星期一");
                break;
            case 2:
                System.out.println("星期二");
                break;
            case 3:
                System.out.println("星期三");
                break;
        }
    }
}
