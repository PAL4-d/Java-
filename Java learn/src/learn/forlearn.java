package learn;

public class forlearn {
    public static void main(String[]args){
        // for (初始化表达式;循环条件;操作表达式)
        // {
        //     执行语句
        // }
        int sum = 0;
        for (int i = 1; i <= 4; i++){
            sum += i;
        }
        System.out.println("sum="+sum);
    }
}
