package learn;

public class ifelselearn {
    public static void main(String[] args){
        // if (判断条件)
        // {
        //     代码块1
        // }
        // else{
        //     代码块2
        // }
        int num = 19;
        if (num %2 ==0){
            // 判断条件成立，num被2整除
            System.out.println("num是一个整数");
        }
        else{
            System.out.println("num是一个奇数");
        }

    }
}
